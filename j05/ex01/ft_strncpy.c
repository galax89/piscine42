char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	int i;

	i = 0;
	while (i != '\0')
	{
		src[i] = dest[i];
		i++;
	}
	if (src[i] == '\0')
		src[i] = dest[i];
	if (i < n)
		dest[i] = '\0';
	return (dest);
}
